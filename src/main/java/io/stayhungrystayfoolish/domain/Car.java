    private String id;

    @Column(name = "price", precision=32, scale=16)
    private BigDecimal price;

    @NotNull
    @Column(name = "createdAt", nullable = false)
    private Long createdAt;

    // jhipster-needle-entity-add-field - JHipster will add fields here, do not remove
